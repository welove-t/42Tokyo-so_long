#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*us1;
	unsigned char	*us2;

	us1 = (unsigned char *)s1;
	us2 = (unsigned char *)s2;
	if (n == 0)
		return (0);
	while (*us1 == *us2)
	{
		n--;
		if (n == 0)
			return (0);
		us1++;
		us2++;
	}
	return (*us1 - *us2);
}
